    vim.cmd('Git')
  end, '[V]ersion Conrol for git')

  -- :Difference
  nmap('<leader>kd', function()
    vim.cmd('Git difftool')
  end, '[D]ifference')
  
  
  -- :Black
  nmap('<leader>kb', function()
    vim.cmd('PyrightOrganizeImports')
    vim.cmd('wa')
    vim.cmd('!black %')
  end, '[B]lack')

-- ChatGPT
  nmap('<leader>ka', function()
    vim.cmd('ChatGPT')
  end, '[A]I')


  --:Fcore KDL
  nmap('<leader>kk', function()
    vim.cmd('e term://=')
  end, '[K]dl')

  -- :Quickfix list 
  nmap('<leader>kq', function()
    vim.cmd('copen')
  end, '[K]Quickfix list')



  -- :Telescope lsp_document_symbols
  nmap('<leader>ks', function()
    require('telescope.builtin').lsp_document_symbols()
  end, '[S]ymbols')


  -- :Terminal
  nmap('<leader>kt', function()
    vim.cmd('terminal')
  end, 'Te[r]minal - open terminal')
  -- Create a command `:Format` local to the LSP buffer
  vim.api.nvim_buf_create_user_command(bufnr, 'Format', function(_)
